package com.example.fitazafitnessapp.model;

public class Timetable {

    private String day;
    private String exercise1;
    private int time1H;
    private int time1M;
    private int time2H;
    private int time2M;
    private String exercise2;
    private String time2;

    public Timetable() {
    }

    public String getDay() {
        return day;
    }

    public void setDay(String day) {
        this.day = day;
    }

    public String getExercise1() {
        return exercise1;
    }

    public void setExercise1(String exercise1) {
        this.exercise1 = exercise1;
    }

    public int getTime1H() {
        return time1H;
    }

    public void setTime1H(int time1H) {
        this.time1H = time1H;
    }

    public int getTime1M() {
        return time1M;
    }

    public void setTime1M(int time1M) {
        this.time1M = time1M;
    }

    public int getTime2H() {
        return time2H;
    }

    public void setTime2H(int time2H) {
        this.time2H = time2H;
    }

    public int getTime2M() {
        return time2M;
    }

    public void setTime2M(int time2M) {
        this.time2M = time2M;
    }

    public String getExercise2() {
        return exercise2;
    }

    public void setExercise2(String exercise2) {
        this.exercise2 = exercise2;
    }

    public String getTime2() {
        return time2;
    }

    public void setTime2(String time2) {
        this.time2 = time2;
    }
}
